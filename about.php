            <p class="lead">
              <strong>International Science Council Regional Office for Africa (ISC ROA)</strong>
            </p>
              <p>The ISC ROA endeavours to ensure that the voice of African scientists influence the international science agenda and that scientists from Africa are fully involved in international research programmes guided by regional priorities. It seeks to promote and increase the participation of African scientists, science organisations and institutions in the programmes and activities of the ISC family and its partners on the continent while widening the ISC membership base in the region.</p>
              <p>After a series of consultative forums, the following priority research areas for the ISC ROA were identified: (a) sustainable energy; (b) natural and human-induced hazards and disasters; (c) health and human wellbeing; and (d) global environmental change (including climate change and adaptation).</p>
              <p>The work around these thematic areas has seen trans-disciplinary, multi-national research consortia formed and working together on project proposals emanating from these four priority areas. However, to further improve the linkages and promote the development and strengthening of collaborative scientific research in Africa, it is paramount that there is a platform for that host information related to the scientists on the continent as well as their institutions, expertise and research themes.</p>
              <p>One of the objectives of ISC ROA is the establishment of a database of African experts in all science fields.</p>

            <span id="assaf"></span>
            <p class="lead">
              <strong>Academy of Science of South Africa (ASSAf)</strong>
            </p>
              <p>The Academy of Science of South Africa (ASSAf) was inaugurated in May 1996 by the former President of South Africa and patron of the Academy, Nelson Mandela.</p>
              <p>It was formed in response to the need for an academy of science congruent with the dawn of democracy in South Africa - activist in its mission of using science for the benefit of society.</p>
              <p>The mandate of the Academy encompasses all fields of scientific enquiry and it includes the full diversity of South Africa’s distinguished scientists. The Parliament of South Africa passed the Academy of Science of South Africa Act (Act 67 of 2001), as amended, which came into operation in May 2002.</p>
              <p>ASSAf is the official national Academy of Science of South Africa and represents the country in the international community of science academies. Since its inception, ASSAf has grown from a small, emergent organisation to a well-established academy. <a href="https://www.assaf.org.za/">Read More...</a></p>

